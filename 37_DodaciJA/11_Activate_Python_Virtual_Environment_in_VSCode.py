# Activate Python Virtual Environment in VSCode

# Kao vsc ima problema ponekad sa aktiviranjem env-a.
# Open folder projekta u kom su mape "env" i "code"
# VSC otvorio u code-mapi i preko terminala ide level up pa u mapu "env" pa u "Scripts" (Winsi) i pokusava sa komandom "activate" aktivirati. Gomila crvenog teksta oznacava neuspio pokusaj.
# rjesenje: csp i izabrati "Python: Select Interpreter", "Select interpreter path" pas "Find" i browsati: "env", "Scripts", "python.exe"
# Zatvorimo vsc-term i otvorimo novi: ispred prompta se pojavljuje (env) - znak aktiviranog env-a!
