# Unit testing
# ____ Test smallest parts of an application in isolation (e.g. units)
# ____ Good candidates for unit testing: individual classes, modules, or functions
# ____ Bad candidates for unit testing: an entire application, dependencies across several classes or modules

# unittest

# ____ Python comes with a built-in module called unittest
# ____ You can write unit tests encapsulated as classes that inherit from unittest.TestCase
# ____ This inheritance gives you access to many assertion helpers that let you test the behavior of your functions
# ____ You can run tests by calling unittest.main()
